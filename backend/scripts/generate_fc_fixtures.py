#!/usr/bin/env python3
"""Synthetic FC-v1 fixture generator (spike protocol: backlog/pi/11_spike_briefs.md §1).

Emits the PRE-BAA, fully-synthetic corpus that feeds the S1 OCR bake-off and (later) the
S2 extraction spike. NOTHING here touches live PHI — every page is drawn from fixed
templates with a synthetic patient ("Jane Sample") and a fixed provider roster.

What it generates under ``<out>/fc_v1_synthetic/``:

  * FC-1  — clean typed EMR-style visit notes (text-layer fast path). For EVERY page it
            also writes the GOLD transcript ``gold/<page_id>.txt`` containing exactly the
            strings drawn onto that page: the generator IS the gold labeler for synthetic
            pages, so the S1 scorer has ground truth for free.
  * FC-B  — CMS-1500-ish itemized bills: header + a drawn line-item table. Alongside each
            PDF, the gold cell grid is written as ``tables/<doc_id>.csv`` (exact strings;
            dollar cells exact to the cent). Line amounts are integer cents internally and
            rendered with integer formatting — never float math (money discipline).
  * FC-2/FC-3 — cannot be generated in software (they require real fax/scan hardware and
            founder-filled forms). This script emits their print-ready SOURCE PDFs plus a
            ``print_sources/README.txt`` explaining the §1 physical protocol, and two
            checkbox/intake-form source PDFs for FC-3.
  * MANIFEST.json — per-file {set, page_count, sha256, seed}; the brief's freeze-as-FC-v1
            discipline (additions create FC-v2, never silent drift).

Determinism: a single ``random.Random(seed)`` drives all sampling. No global ``random``,
no wall-clock. Same seed + sizes => byte-identical PDFs => identical MANIFEST hashes.

Ownership: M1 Wave D spike S1 tool. Lives under backend/scripts/ (not a package); not
imported by app.*.
"""

