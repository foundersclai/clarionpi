def _fmt_dollars(cents: int) -> str:
    """Render integer cents as ``$1,234.56`` with NO float math.

    Uses integer division/modulo for the dollars/cents split, then comma-groups the dollar
    part by hand. A wrong cent here would fail an S1 table cell, so this must be exact.
    """
    dollars, remainder = divmod(cents, 100)
    return f"${dollars:,}.{remainder:02d}"


@dataclass(frozen=True)
class _ManifestEntry:
    set_name: str
    page_count: int
    sha256: str
    seed: int


def _sha256(path: Path) -> str:
    return hashlib.sha256(path.read_bytes()).hexdigest()


def _gold_header(generator: str) -> str:
    """Provenance banner prepended to every gold transcript (auditable, synthetic)."""
    return (
        f"# generated by {generator}, synthetic, no PHI\n"
        f"# gold transcript — exact strings drawn on the corresponding page\n"
    )


# --------------------------------------------------------------------------------------
# PDF drawing helpers (deterministic; no timestamps => reproducible bytes)
# --------------------------------------------------------------------------------------
def _new_canvas() -> tuple[canvas.Canvas, io.BytesIO]:
    """A reportlab canvas writing to an in-memory buffer.

    ``invariant=1`` strips the PDF's producer/creation-date metadata so identical draw
    calls yield byte-identical output — the determinism the freeze discipline needs.
    """
    buffer = io.BytesIO()
    pdf = canvas.Canvas(buffer, pagesize=letter, invariant=1)
    pdf.setTitle("FC-v1 synthetic fixture")
    return pdf, buffer


def _write_pdf(buffer: io.BytesIO, path: Path) -> None:
    path.write_bytes(buffer.getvalue())


# --------------------------------------------------------------------------------------
# FC-1: clean EMR visit notes + gold transcripts
# --------------------------------------------------------------------------------------
def _emr_page_lines(rng: random.Random, page_index: int) -> list[str]:
    """The exact text lines for one FC-1 page (drives BOTH the PDF and its gold file)."""
    provider = rng.choice(_PROVIDERS)
    facility = rng.choice(_FACILITIES)
    complaint = rng.choice(_CHIEF_COMPLAINTS)
    exam = rng.choice(_EXAM_LINES)
    assessment = rng.choice(_ASSESSMENTS)
    plan = rng.choice(_PLANS)
    # Synthetic, stable date; never wall-clock.
    dos_day = 1 + (page_index % 27)
    return [
        f"PATIENT: Jane Sample    DOB: 1985-03-14    MRN: SYNTH-{page_index:04d}",
        f"DATE OF SERVICE: 2024-02-{dos_day:02d}    PROVIDER: {provider}",
        f"FACILITY: {facility}",
        "CHIEF COMPLAINT",
        complaint,
        "HISTORY",
        "Patient reports symptoms began following a motor vehicle collision.",
        "EXAM",
        exam,
        "ASSESSMENT",
        assessment,
        "PLAN",
        plan,
    ]


def _emit_fc1(rng: random.Random, out_root: Path, pages: int, seed: int) -> list[_ManifestEntry]:
    fc1_dir = out_root / "fc1_clean_emr"
    gold_dir = fc1_dir / "gold"
    fc1_dir.mkdir(parents=True, exist_ok=True)
    gold_dir.mkdir(parents=True, exist_ok=True)

    entries: list[_ManifestEntry] = []
    generator = "generate_fc_fixtures.py"
    for i in range(pages):
        page_id = f"fc1_p{i:04d}"
        text_lines = _emr_page_lines(rng, i)

        pdf, buffer = _new_canvas()
        y = 720
        for line in text_lines:
            # Section headers (CHIEF COMPLAINT / HISTORY / EXAM / ASSESSMENT / PLAN) render bold.
            is_header = line in {"CHIEF COMPLAINT", "HISTORY", "EXAM", "ASSESSMENT", "PLAN"}
            pdf.setFont(_FONT_BOLD if is_header else _FONT, 13 if is_header else 11)
            pdf.drawString(72, y, line)
            y -= 26
        pdf.showPage()
        pdf.save()
        pdf_path = fc1_dir / f"{page_id}.pdf"
        _write_pdf(buffer, pdf_path)

        # Gold transcript: provenance header + exactly the strings drawn on the page.
        gold_path = gold_dir / f"{page_id}.txt"
        gold_path.write_text(
            _gold_header(generator) + "\n".join(text_lines) + "\n", encoding="utf-8"
        )

        entries.append(_ManifestEntry("FC-1", 1, _sha256(pdf_path), seed))
    return entries


# --------------------------------------------------------------------------------------
# FC-B: CMS-1500-ish itemized bills + gold cell grids
# --------------------------------------------------------------------------------------
def _bill_grid(rng: random.Random, doc_index: int) -> list[list[str]]:
    """The gold cell grid for one FC-B bill: header row + line rows + total row.

    Columns: Date of Service | CPT | Description | Billed. Dollar cells are cent-exact
    strings; the total is an integer-cents sum, never float-accumulated.
    """
    n_lines = 3 + (doc_index % 3)  # 3..5 line items, deterministic
    chosen = [_BILL_LINES[(doc_index + k) % len(_BILL_LINES)] for k in range(n_lines)]

    grid: list[list[str]] = [["Date of Service", "CPT", "Description", "Billed"]]
    total_cents = 0
    for k, (cpt, desc, cents) in enumerate(chosen):
        dos_day = 1 + ((doc_index + k) % 27)
        grid.append([f"2024-03-{dos_day:02d}", cpt, desc, _fmt_dollars(cents)])
        total_cents += cents  # integer cents only
    grid.append(["", "", "TOTAL", _fmt_dollars(total_cents)])
    return grid


def _emit_fcb(rng: random.Random, out_root: Path, docs: int, seed: int) -> list[_ManifestEntry]:
    fcb_dir = out_root / "fcb_bills"
    tables_dir = fcb_dir / "tables"
    fcb_dir.mkdir(parents=True, exist_ok=True)
    tables_dir.mkdir(parents=True, exist_ok=True)

    entries: list[_ManifestEntry] = []
    for i in range(docs):
        doc_id = f"fcb_b{i:04d}"
        grid = _bill_grid(rng, i)

        pdf, buffer = _new_canvas()
        pdf.setFont(_FONT_BOLD, 14)
        pdf.drawString(72, 740, "ITEMIZED STATEMENT (CMS-1500 style)")
        pdf.setFont(_FONT, 11)
        pdf.drawString(72, 720, f"Patient: Jane Sample    Account: SYNTH-BILL-{i:04d}")
        pdf.drawString(72, 704, "Facility: Riverbend Family Clinic")

        # Draw the grid as a simple fixed-column table.
        col_x = (72, 190, 260, 470)
        y = 670
        for row in grid:
            for cell, x in zip(row, col_x, strict=False):
                pdf.setFont(_FONT_BOLD if y == 670 or cell == "TOTAL" else _FONT, 11)
                pdf.drawString(x, y, cell)
            y -= 22
        pdf.showPage()
        pdf.save()
        pdf_path = fcb_dir / f"{doc_id}.pdf"
        _write_pdf(buffer, pdf_path)

        # Gold cell grid: exact strings, dollar cells exact.
        csv_path = tables_dir / f"{doc_id}.csv"
        with csv_path.open("w", encoding="utf-8", newline="") as handle:
            csv.writer(handle).writerows(grid)

        entries.append(_ManifestEntry("FC-B", 1, _sha256(pdf_path), seed))
    return entries


